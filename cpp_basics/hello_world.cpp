#include <iostream>  // standard stream I/O facilities are conatined in iostream
using namespace std; // makes name from std visible without using std:: infront of every name

int main()
{
    // cout is the name defined in the std namespace
    cout << "Hello World!"; // << operator means 'put to'. It writes the second argument into the first one, in this case our string hello world is being put in to the standard output stream
}